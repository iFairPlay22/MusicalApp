import Vue from "vue";

// ######################################################################################################################### //
// ######################################################## GLOBALS ######################################################## //
// ######################################################################################################################### //

export const globals = Vue.observable({
  snacks: [],
});

// ######################################################################################################################### //
// ######################################################## GETTERS ######################################################## //
// ######################################################################################################################### //

export const getters = {
  snacks: () => globals.snacks,
};

// ######################################################################################################################### //
// ####################################################### MUTATIONS ####################################################### //
// ######################################################################################################################### //

export const mutations = {
  addSnack: (val) => {
    globals.snacks.push(val);
  },
  removeSnack: (val) =>
    (globals.snacks = globals.snacks.filter((value) => value != val)),
};

